public class BooleanRelational {
    public static void main(string[] args) {
        int num1 = 5;
        int num2 = 10;
        boolean a = num1 < num2;
        boolean b = num1 > num2;
        boolean c = num1 <= num2;
        boolean d = num1 >= num2;
        boolean e = num1 == num2;
        boolean f = num1 != num2;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}

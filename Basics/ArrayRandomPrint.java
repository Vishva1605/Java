public class ArrayRandomPrint {
    public static void main(string[] args) {
        int num[] = {1,2,3,4,5};
        int k = 3;
        for(int i=k;i<num.length;i++){
            System.out.print(num[i]+" ");    
        }
        for(int i=0;i<k;i++){
            System.out.print(num[i]+" ");
        }
    }
}

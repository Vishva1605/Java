public class Conversion {
    public static void main(string[] args) {
        byte a = 100;
        int b = a;
        float c = b;
        double d = b;
        char ch = 'd';
        int i   = ch;
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(ch);
        System.out.println(i);
    }
}

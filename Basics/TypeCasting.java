class TypeCasting {
    public static void main(string[] args) {
        int a = 122;
        byte b = (byte)a;
        System.out.println(b);
        float f = 8.6f;
        int i = (int)f;
        System.out.println(i);
        double d = 22.976;
        int c = (int)d;
        System.out.println(c);
        int n = 68;
        char ch = (char)n;
        System.out.println(ch);
    }
}
